import java.util.*;
class ArithOperators {
    public static void main(String[] args) {
        int x, y;
        
        x = 9800;
        y = 324234;
    
        System.out.print("X = ");
        System.out.print(x);
        System.out.print(", Y = ");
        System.out.println(y);
    
        System.out.print("Add ");
        System.out.println(x + y);
        System.out.print("Sub ");
        System.out.println(x - y);
        System.out.print("Mul ");
        System.out.println(x * y);
        System.out.print("Div ");
        System.out.println(x / y);
        System.out.print("Mod ");
        System.out.println(x % y);
    }
}
